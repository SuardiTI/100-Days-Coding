package Day73;

public class Main{
    
    public static void main(String[]args){
        //Concatenation/penggabungan
      
        String namaDepan = "Kurnia";
        String namaBelakang = "Pratiwi";
        
        // Menggabungkan string dengan metode concat()
        String hasilPenggabungan = namaDepan.concat(" ").concat(namaBelakang);
        System.out.println(hasilPenggabungan);
           
     }
}
