package Day61;

public class Main {
    //fungsi untuk menjumlah
    public static int penjumlahan(int x, int y){
        return x + y;
    }
    
    public static void main(String[] args) {
        //memanggil fungsi penjumlahan
        int hasilJumlah = penjumlahan(5, 6);
        System.out.println("Hasil Penjumlahan : "+hasilJumlah);
    }
    
}
