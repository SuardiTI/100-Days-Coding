package Day29;

public class Main {
    
    public static void main(String[] args) {
        //operator perbandingan
        int nilai = 8;
        int angka = 6;
        System.out.println(nilai == angka);
        System.out.println(nilai != angka);
        System.out.println(nilai > angka);
        System.out.println(angka <= nilai);
        
        //operator logika
        boolean a = true;
        boolean b = false;
        System.out.println(a && b);
        System.out.println(a || b);
        System.out.println(!a);
        System.out.println(!b);
    }
    
}
